#################################################################################
#   Function Name   :   Display
#   Input           :   Integer 
#   Output          :   Integer
#   Description     :   Display 10 to 1
#   Author          :   Rushikesh Hemant Gholap
#   Date/Time       :   11/05/2024
#################################################################################
def Display(No):

    for i in range(No, 0, -1):
        print(i,end="\t")

#################################################################################
#   Entery Point Function
#################################################################################
def main():

    print("Enter the number :",end="")
    Value = int(input())

    Display(Value)

    return 0
#################################################################################
#   Starter
#################################################################################
if __name__ == "__main__":
    main()

