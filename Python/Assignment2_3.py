def Factorial(No):
    
    Fact = 1
    
    for i in range(1, No+1):
        Fact = Fact * i
        
    return Fact

#################################################################################
#   Entery Point Function
#################################################################################
def main():

    print("Enter the number : ",end="")
    Value = int(input())
    
    Result = Factorial(Value)
    
    print("Factorial of ",Value, ": ",Result)
    return 0

#################################################################################
#   Starter
#################################################################################
if __name__ == "__main__":
    main()

