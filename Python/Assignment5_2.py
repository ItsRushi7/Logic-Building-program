#   Problem Statement :  Recursive program which disply below pattern
#################################################################################

Cnt = 1
#################################################################################
#   Function Name   :   Display
#   Input           :   Integer
#   Output          :   Integer
#   Description     :   Disply below pattern
#   Author          :   Rushikesh Hemant Gholap
#   Date/Time       :   11/05/2024
#################################################################################
def Display(No):

    global Cnt 
    
    if (No >= Cnt):
        print(Cnt ,end=" ")
        Cnt = Cnt + 1
        Display(No)


#################################################################################
#   Entery Point Function
#################################################################################
def main():

    print("Enter the number :", end="")
    Value = int(input())

    Display(Value)

    return 0


#################################################################################
#   Starter
#################################################################################
if __name__ == "__main__":
    main()

