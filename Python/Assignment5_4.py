#   Problem Statement :  Recursive program to return summation of digits
#################################################################################


#################################################################################
#   Function Name   :   Summation
#   Input           :   Integer
#   Output          :   Integer
#   Description     :   Summation of digit
#   Author          :   Rushikesh Hemant Gholap
#   Date/Time       :   11/05/2024
#################################################################################


def Summation(No):

    if No == 0:
        return 0

    return (No % 10 + Summation(int(No / 10)))


#################################################################################
#   Entery Point Function
#################################################################################


def main():

    print("Enter the number :", end="")
    Value = int(input())

    Result = Summation(Value)

    print("Summation of Digit : ", Result)

    return 0


#################################################################################
#   Starter
#################################################################################
if __name__ == "__main__":
    main()

