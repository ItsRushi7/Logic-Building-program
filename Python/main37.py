def Display(No):
    
    if (No >= 1):
        print(No ,end=" ")
        No = No - 1
        Display(No)


#################################################################################
#   Entery Point Function
#################################################################################
def main():

    print("Enter the number :", end="")
    Value = int(input())

    Display(Value)

    return 0


